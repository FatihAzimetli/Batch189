package day03wrapperconcatenationoperators;

public class TypeCasting {
    public static void main(String[] args) {
        /*primitive data tiplerinin birbirine dönüstürülmesine type Casting denir
        * OTO WIDENING "otomatik genisletme"
        * EXPLICIT NORROWING "aciktan daraltma"*/


        //byte data tipini int data tipine ceviriniz

        byte age = 13;
        int ageInt = age; //AUTO WIDENING


        // int data tipini short data tipine ceviriniz

        int weight = 313;
        short wieghtShort = (short) weight; //EXPLICIT NARROWING






    }
}
